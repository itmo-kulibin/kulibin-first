    <h1><b>«Подъемные кресла» (лифт)</b></h1>
    <p>В конце XVIII века Иван Кулибин разработал механизм, позволявший поднимать человека на верхние этажи без использования лестниц. Его конструкция, созданная для удобства передвижения по дворцовым помещениям, стала одним из ранних прообразов современного лифта.</p>
    <p>Идея подъема людей и грузов существовала задолго до Кулибина. В древнем Риме для этой цели применялись примитивные устройства на основе блоков и канатов, приводимые в движение мускульной силой. В средневековых замках встречались механизмы для перемещения грузов между этажами. Однако полноценные лифтовые конструкции оставались редкостью вплоть до XVIII века.</p>
    <p>Екатерина II пожелала иметь устройство, которое позволяло бы ей перемещаться между этажами дворца без необходимости подниматься по лестнице.</p>
    <p>Разработанный для решения этой задачи Кулибиным механизм представлял собой подъемное кресло, которое помещалось на гайки. Эти гайки двигались по двум вертикально установленным столбам. Несколько рабочих, сидящих за кабиной, приводили в действие механизм, вращая гайки с помощью системы шестерен. Такое устройство плавно и безопасно поднимало императрицу на верхние этажи дворца.</p>
    <p>Кулибин применил несколько передовых для своего времени инженерных решений. Важным новшеством стали подшипники (чем-то похожие на те, которые используются в современных машинах), которые уменьшали трение в механизме. В качестве смазки изобретатель использовал графит, а не сало, как было принято. Дополнительно была продумана система безопасности: в случае обрыва противовеса устройство должно было автоматически останавливать движение, предотвращая падение кресла.</p>
