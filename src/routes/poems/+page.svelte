    <p>Важной особенностью стиля Кулибина является простота языка, которая может быть неочевидной современному читателю.</p>
    <p>В начале второй половины XVIII века Ломоносов разделил все слова, которые употребляются в литературе, на три категории: те, которые есть и в русском, и в церковнославянском и известны всем; церковнославянские, которые известны всем грамотным людям; и, наконец, слова, которых в церковнославянском языке нет.</p>
    <p>В высоком штиле позволялось употреблять слова только первого и второго рода, и эта литературная норма изменилась только при реформах Карамзина.
    <br>Поэзия Кулибина относится к периоду до этого культурного сдвига. Он нарушает установленные правила и использует слова третьего рода. Его поэзия лишена придворного лоска и тяжеловесности, но при этом не уходит в просторечные народные мотивы, которые их сменят.</p>
    <p>В наше время его поэтические труды остаются малоизвестными, но они дополняют образ великого мастера, чьё имя стало нарицательным.</p>
