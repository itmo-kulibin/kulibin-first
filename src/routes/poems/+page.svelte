            <EmbeddedBlockText slot="short"><p class="poem-text">“...Мешает правильному ходу
                То, что употребив свободу,
                Стремится слабый человек
                Замедлить мира плавный бег...”.
                
                И. П. Кулибин “В ознаменование Светлого Господнего воскресения 1764 г”</p>
