    <p>На протяжении всего XVIII века жанр поэзии претерпевал значительные изменения и был предметом оживлённых дискуссий среди образованного общества.
    <br>Эстетические идеи того времени предполагали строгое разделение поэтических жанров на «низкие», «средние» и «высокие», причём последние, включавшие в себя жанр оды, ценились среди высшего сословия больше всего.</p>
    <p>Обычно оды посвящались государству, значимым событиям или выдающимся личностям и часто содержали библейские и античные мотивы. Характерной чертой поэзии Кулибина стало добавление к этим традиционным темам ещё одной — значимости знания. Не менее важным для него был и вопрос веры. В результате в его произведениях строки, посвященные духовному восприятию мира, нередко соседствуют с размышлениями о науке. В его творчестве чувствуется влияние идей просвещенного абсолютизма.</p>
